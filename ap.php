            <p>I. El apercibimiento para que el responsable lleve a cabo los actos solicitados por el titular, en los términos previstos por esta Ley, tratándose de los supuestos previstos en la fracción I del artículo anterior; </p>

            <p>II. Multa de 100 a 160,000 días de salario mínimo vigente en el Distrito Federal, en los casos previstos en las fracciones II a VII del artículo anterior; </p>

            <p>III. Multa de 200 a 320,000 días de salario mínimo vigente en el Distrito Federal, en los casos previstos en las fracciones VIII a XVIII del artículo anterior, y </p>

            <p>IV. En caso de que de manera reiterada persistan las infracciones citadas en los incisos anteriores, se impondrá una multa adicional que irá de 100 a 320,000 días de salario mínimo vigente en el Distrito Federal. En tratándose de infracciones cometidas en el tratamiento de datos sensibles, las sanciones podrán incrementarse hasta por dos veces, los montos establecidos. </p>

            <p>Artículo 65.- El Instituto fundará y motivará sus resoluciones, considerando: I. La naturaleza del dato; </p>

            <p>II. La notoria improcedencia de la negativa del responsable, para realizar los actos solicitados por el titular, en términos de esta Ley; </p>

            <p>III. El carácter intencional o no, de la acción u omisión constitutiva de la infracción; </p>

            <p>IV. La capacidad económica del responsable, y </p>

            <p>V. La reincidencia. </p>

            <p>Artículo 66.- Las sanciones que se señalan en este Capítulo se impondrán sin perjuicio de la responsabilidad civil o penal que resulte. </p>

            <p>CAPÍTULO XI<br />
                De los Delitos en Materia del Tratamiento Indebido de Datos Personales </p>

            <p>Artículo 67.- Se impondrán de tres meses a tres años de prisión al que estando autorizado para tratar datos personales, con ánimo de lucro, provoque una vulneración de seguridad a las bases de datos bajo su custodia. </p>

            <p>Artículo 68.- Se sancionará con prisión de seis meses a cinco años al que, con el fin de alcanzar un lucro indebido, trate datos personales mediante el engaño, aprovechándose del error en que se encuentre el titular o la persona autorizada para transmitirlos. </p>

            <p>Artículo 69.- Tratándose de datos personales sensibles, las penas a que se refiere este Capítulo se duplicarán. </p>
